# DataType  str
x = "Hello World"
print(type(x))

# DataType int
x = 50
print(type(x))

# DataType float
x = 60.5
print(type(x))

# DataType complex
x = 3j
print(type(x))

# DataType list
x = ["geeks", "for", "geeks"]
print(type(x))

# DataType tuple
x = ("geeks", "for", "geeks")
print(type(x))

# DataType dict
x = {"name": "Suraj", "age": 24}
print(type(x))

# DataType set
x = {"geeks", "for", "geeks"}
print(type(x))

# DataType bool
x = True
print(type(x))

